/**
 * Represents the state of keyboard controls for the game.
 */
class Keyboard {
    /**
     * Indicates whether the left arrow key is currently pressed.
     * @type {boolean}
     */
    LEFT = false;

    /**
     * Indicates whether the right arrow key is currently pressed.
     * @type {boolean}
     */
    RIGHT = false;

    /**
     * Indicates whether the spacebar key is currently pressed.
     * @type {boolean}
     */
    SPACE = false;

    /**
     * Indicates whether the 'D' key is currently pressed.
     * @type {boolean}
     */
    D = false;
}